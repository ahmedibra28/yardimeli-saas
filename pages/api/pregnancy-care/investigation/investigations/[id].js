import nc from 'next-connect'
import db from '../../../../../config/db'
import Investigation from '../../../../../models/pregnancy-care/Investigation'
import { isAuth } from '../../../../../utils/auth'

const schemaName = Investigation
const schemaNameString = 'Investigation'

const handler = nc()
handler.use(isAuth)
handler.put(async (req, res) => {
  await db()
  try {
    const { id } = req.query
    const object = await schemaName.findById(id)
    if (!object)
      return res.status(400).json({ error: `${schemaNameString} not found` })

    object.date = req.body.date
    await object.save()

    res.status(200).json({ message: `${schemaNameString} updated` })
  } catch (error) {
    res.status(500).json({ error: error.message })
  }
})

handler.delete(async (req, res) => {
  await db()
  try {
    const { id } = req.query
    const object = await schemaName.findById(id)
    if (!object)
      return res.status(400).json({ error: `${schemaNameString} not found` })

    if (object.status === 'completed')
      return res.status(400).json({
        error: `${schemaNameString} can only be deleted if it is pending`,
      })

    await object.remove()
    res.status(200).json({ message: `${schemaNameString} removed` })
  } catch (error) {
    res.status(500).json({ error: error.message })
  }
})

export default handler
